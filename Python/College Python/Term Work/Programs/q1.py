#float variable
my_float = 5.5
print(my_float,"is of type",type(my_float),"and id is",id(my_float),"\n")

#string variable
my_string = "Hello"
print(my_string,"is of type",type(my_string),"and id is",id(my_string),"\n")

#boolean variable
my_bool = True
print(my_bool,"is of type",type(my_bool),"and id is",id(my_bool),"\n")

#list variable
my_list = [1,2,3]
print(my_list,"is of type",type(my_list),"and id is",id(my_list),"\n")

#tuple variable
my_tuple = (1,2,3)
print(my_tuple,"is of type",type(my_tuple),"and id is",id(my_tuple),"\n")

