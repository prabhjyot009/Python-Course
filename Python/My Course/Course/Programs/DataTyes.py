'''Data Types'''
# 1. Numbers
# 1.1 Integers
a = 10
b = 20
c = a + b
print(c)
# 1.2 Float
a = 10.5
b = 20.5
c = a + b
print(c)
# 1.3 Complex
a = 10 + 5j
b = 20 + 5j
c = a + b
print(c)
# 2. Strings:
a = "Hello"
b = "World"
c = a + b
print(c)
# 3. List
a = [10, 20, 30, 40, 50]
b = [60, 70, 80, 90, 100]
c = a + b
print(c)
# 4. Tuple
a = (10, 20, 30, 40, 50)
b = (60, 70, 80, 90, 100)
c = a + b
print(c)
# 5. Set
a = {10, 20, 30, 40, 50}
b = {60, 70, 80, 90, 100}
c = a and b
print(id(a))
print(c)
# 6. Dictionary
a = {"Name": "Singh", "Age": 19, "Country": "India"}
b = {"Name": "Singh", "Age": 19, "Country": "India"}
c = a and b
print(c)
# 7. Boolean
print(10 > 20)

x=bool(10)  #0 and null are false
print(x)

